/**
 * @brief USART3中断处理函数 (ESP8266)
 */
void USART3_IRQHandler(void)
{
    uint8_t data;

    /* 处理溢出错误 */
    if(__HAL_UART_GET_FLAG(&huart3, UART_FLAG_ORE) != RESET)
    {
        __HAL_UART_CLEAR_OREFLAG(&huart3);
        (void)huart3.Instance->SR;
        (void)huart3.Instance->DR;
    }

    /* 接收中断 */
    if(__HAL_UART_GET_FLAG(&huart3, UART_FLAG_RXNE) != RESET)
    {
        data = (uint8_t)(huart3.Instance->DR & 0xFF);

        if(esp8266_rx_cnt < ESP8266_RX_BUF_SIZE - 1)
        {
            esp8266_rx_buf[esp8266_rx_cnt++] = data;
        }
        else
        {
            /* 缓冲区满，重置 */
            esp8266_rx_cnt = 0;
        }
    }

    /* 空闲中断 - 表示一帧数据接收完成 */
    if(__HAL_UART_GET_FLAG(&huart3, UART_FLAG_IDLE) != RESET)
    {
        /* 清除空闲中断标志 */
        __HAL_UART_CLEAR_IDLEFLAG(&huart3);

        /* 添加字符串结束符 */
        if(esp8266_rx_cnt < ESP8266_RX_BUF_SIZE)
        {
            esp8266_rx_buf[esp8266_rx_cnt] = '\0';
        }
    }
}

